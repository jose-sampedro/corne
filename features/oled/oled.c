static void render_oled_layer(int position_x, int position_y) {
    oled_set_cursor(position_x, position_y);

    switch (get_highest_layer(layer_state)) {
        case _M1:
            oled_write_P(PSTR("M1"), false);
            break;
        case _M2:
            oled_write_P(PSTR("M2"), false);
        break;
        case _M3:
            oled_write_P(PSTR("M3"), false);
        break;
        case _M4:
            oled_write_P(PSTR("M3"), false);
        break;
        default:
            oled_write_P(PSTR("M0"), false);
        break;
    }
}

static void render_oled_master(void) {
    render_oled_layer(2, 2);

    oled_set_cursor(0, 5);

    render_mod_status_gui_alt(get_mods()|get_oneshot_mods());
    render_mod_status_ctrl_shift(get_mods()|get_oneshot_mods());

    render_megaman(0, 13);
}


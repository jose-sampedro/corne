static void render_oled_wpm(int position_x, int position_y){
    oled_set_cursor(position_x, position_y);
    oled_write("WPM", false);
    oled_set_cursor(1, 13);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + wpm_count % 10;
    wpm_str[1] = '0' + (wpm_count /= 10) % 10;
    wpm_str[0] = '0' + wpm_count / 10;
    oled_write(wpm_str, false);
}

static void render_oled_slave(void) {
    render_oled_wily(0, 4);
    render_oled_wpm(1, 12);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) { 
    return OLED_ROTATION_270; 
}

bool oled_task_user(void) {
    led_usb_state = host_keyboard_led_state();
    wpm_count = get_current_wpm();

    if (is_keyboard_master()) {
        render_oled_master();
    } else {
        render_oled_slave();
    }

    return false;
}